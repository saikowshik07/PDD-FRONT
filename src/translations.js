export function getTranslation(lang, key, params = {}) {
  const targetLang = lang || 'en';
  const k = (key || '').toLowerCase().trim();
  let translation = key;
  if (translations[targetLang] && translations[targetLang][k]) {
    translation = translations[targetLang][k];
  } else if (translations['en'] && translations['en'][k]) {
    // fallback to English
    translation = translations['en'][k];
  }
  for (let p in params) {
    translation = translation.replace(`{${p}}`, params[p]);
  }
  return translation;
}
